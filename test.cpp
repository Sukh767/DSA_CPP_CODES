#include<iostream>
using namespace std;

int main()
{
    // Unary Operator
    int a = 10;
    cout << "Unary Operator" << endl;
    cout << "a = " << a << endl;
    cout << "++a = " << ++a << endl;
    cout << "a = " << a << endl;
    cout << "a++ = " << a++ << endl;
    cout << "a = " << a << endl;

    // Unary Operator decrement
    cout << "--a = " << --a << endl;
    cout << "a = " << a << endl;
    cout << "a-- = " << a-- << endl;
    cout << "a = " << a << endl;
    
}